#!/usr/bin/env python3
"""
ProSA Wrapper (Local + Web Hybrid)
----------------------------------

This wrapper runs the ProSA protein structure validation tool.
It first tries to use the local ProSA2003 binary; if not found, it falls back to the ProSA-web service.

Outputs:
 - prosa_summary.json : JSON summary (Z-score, mode, file paths)
 - prosa.txt          : text log
 - energy_plot.png    : energy profile image
 - energy_plot.svg    : same plot in vector format
"""
import argparse
import os
import json
import shutil
import subprocess
import re
import requests
from bs4 import BeautifulSoup



def run_local_prosa(pdb_file, output_dir, prosa_dir):
    """Run local ProSA2003 binary."""
    exe_name = "PROSAMAIN.exe" if os.name == "nt" else "prosamain"
    prosa_exe = os.path.join(prosa_dir, "bin", exe_name)
    if not os.path.exists(prosa_exe):
        print(f"[ProSA] Local executable not found at {prosa_exe}")
        return None

    print(f"[ProSA] Found local executable: {prosa_exe}")
    print(f"[ProSA] Running local ProSA on {pdb_file}")

    try:
        subprocess.run([prosa_exe, pdb_file], cwd=output_dir, check=True)
    except subprocess.CalledProcessError as e:
        print(f"[ProSA] Error running local ProSA: {e}")
        return None

    # Find the output file (e.g., 2LZM.pdb.prosa)
    prosa_out_file = os.path.join(output_dir, os.path.basename(pdb_file) + ".prosa")
    if not os.path.exists(prosa_out_file):
        print(f"[ProSA] Local ProSA output file not found: {prosa_out_file}")
        return None

    z_score = None
    with open(prosa_out_file, "r") as f:
        for line in f:
            if "Z-SCORE" in line:
                try:
                    z_score = float(line.split()[-1])
                    break
                except (ValueError, IndexError):
                    continue

    summary = {
        "mode": "local",
        "z_score": z_score,
        "zplot_hr_png": None, # Not generated by local version
        "energy_plot_png": None, # Not generated by local version
        "energy_plot_svg": None
    }
    return summary

def run_web_prosa(pdb_file, output_dir):
    """Submit to ProSA-web and parse results."""
    print("[ProSA] Using ProSA-web...")
    os.makedirs(output_dir, exist_ok=True)
    url = "https://prosa.services.came.sbg.ac.at/prosa.php"
    files = {"userfile": open(pdb_file, "rb")}
    data = {
        "max_file_size": "26214400",
        "submButton": "Analyse"
    }
    try:
        response = requests.post(url, files=files, data=data, timeout=120)
    except Exception as e:
        print(f"[ProSA] Web request failed: {e}")
        return None
    if response.status_code != 200:
        print(f"[ProSA] Error: server returned {response.status_code}")
        return None

    soup = BeautifulSoup(response.text, "html.parser")
    zscore = None
    energy_plot_png_path = None
    zplot_hr_png_path = None # New variable for high-res Z-plot

    # Parse Z-score
    zscore_span = soup.find("span", class_="zscore")
    if zscore_span:
        zscore = float(zscore_span.text)

    # Find high-resolution Z-plot image (overall quality plot)
    hr_zplot_link = soup.find("a", {"href": re.compile(r"hr_zplot_.*\.png")})
    if hr_zplot_link and hr_zplot_link.get("href"):
        hr_zplot_url = "https://prosa.services.came.sbg.ac.at/" + hr_zplot_link["href"]
        try:
            img_data = requests.get(hr_zplot_url).content
            zplot_hr_png_output_path = os.path.join(output_dir, "zplot_hr.png")
            with open(zplot_hr_png_output_path, "wb") as f:
                f.write(img_data)
            zplot_hr_png_path = zplot_hr_png_output_path
        except Exception as e:
            print(f"[ProSA] Warning: failed to download high-res Z-plot ({e})")

    # Find energy plot image (local quality plot)
    # The interactive plot on the website is generated using JavaScript and cannot be downloaded as a static file.
    # We will download the static PNG image of the energy plot.
    eplot_link = soup.find("a", {"href": re.compile(r"eplot_.*\.png")})
    if eplot_link and eplot_link.get("href"):
        eplot_url = "https://prosa.services.came.sbg.ac.at/" + eplot_link["href"]
        try:
            img_data = requests.get(eplot_url).content
            eplot_png_output_path = os.path.join(output_dir, "energy_plot.png")
            with open(eplot_png_output_path, "wb") as f:
                f.write(img_data)
            energy_plot_png_path = eplot_png_output_path
        except Exception as e:
            print(f"[ProSA] Warning: failed to download energy plot ({e})")

    with open(os.path.join(output_dir, "prosa.txt"), "w") as f:
        f.write(f"Z-score: {zscore}\n")

    summary = {
        "mode": "web",
        "z_score": zscore,
        "zplot_hr_png": os.path.abspath(zplot_hr_png_path) if zplot_hr_png_path else None,
        "energy_plot_png": os.path.abspath(energy_plot_png_path) if energy_plot_png_path else None,
        "energy_plot_svg": None # SVG plot is not available from the web server
    }
    return summary

def run_prosa(pdb_file, output_dir, prosa_dir=None):
    os.makedirs(output_dir, exist_ok=True)
    summary = None
    if prosa_dir:
        summary = run_local_prosa(pdb_file, output_dir, prosa_dir)

    if not summary:
        summary = run_web_prosa(pdb_file, output_dir)

    if not summary:
        print("[ProSA] Failed to run ProSA.")
        return 1

    summary["input_pdb"] = os.path.abspath(pdb_file)
    with open(os.path.join(output_dir, "prosa_summary.json"), "w") as f:
        json.dump(summary, f, indent=2)
    print(f"[ProSA] Completed. Results written to {output_dir}")
    return 0

def main():
    parser = argparse.ArgumentParser(description="Run ProSA validation on a PDB file")
    parser.add_argument("--pdb_file", required=True, help="Input PDB file")
    parser.add_argument("--output_dir", required=True, help="Output directory for results")
    parser.add_argument("--prosa_dir", help="Path to local ProSA installation directory")
    args = parser.parse_args()
    run_prosa(args.pdb_file, args.output_dir, args.prosa_dir)

if __name__ == "__main__":
    main()